def remove_duplicates(lst):
    """
    리스트(lst)에서 중복 원소를 제거하고 오름차순으로 정렬된 리스트를 반환하는 함수이다.
    세트(set)는 중복 원소를 허용하지 않는 특성을 이용하여 중복을 제거하시오.
    (힌트: set()으로 중복 제거 후 sorted()로 정렬하시오)
    함수에 사용되는 매개변수를 다음과 같이 설정하시오:
      lst: 중복 원소가 포함될 수 있는 리스트
    인자:
      lst (list): 중복 원소가 포함될 수 있는 리스트
    반환값:
      list: 중복이 제거되고 오름차순으로 정렬된 리스트
    예시:
      >>> remove_duplicates([3, 1, 4, 1, 5, 9, 2, 6, 5, 3])
          [1, 2, 3, 4, 5, 6, 9]
    """
    # ===== Your Code Here =====
    return sorted(set(lst))
    # ==========================

def count_unique(lst1, lst2):
    """
    두 리스트(lst1, lst2)를 합쳤을 때 고유한(unique) 원소의 개수를 반환하는 함수이다.
    두 리스트에 공통으로 있는 원소는 한 번만 세어야 한다.
    (힌트: set(lst1 + lst2)로 합집합 원소를 구한 뒤 len()으로 세시오)
    함수에 사용되는 매개변수를 다음과 같이 설정하시오:
      lst1: 첫 번째 리스트
      lst2: 두 번째 리스트
    인자:
      lst1 (list): 첫 번째 리스트
      lst2 (list): 두 번째 리스트
    반환값:
      int: 두 리스트를 합쳤을 때 고유한 원소의 개수
    예시:
      >>> count_unique([1, 2, 3, 2], [3, 4, 5, 4])
          5
    """
    # ===== Your Code Here =====
    return len(set(lst1 + lst2))
    # ==========================

if __name__ == '__main__':
    lst = [3, 1, 4, 1, 5, 9, 2, 6, 5, 3]
    #### 함수 호출 ####
    print(f'중복 제거: {remove_duplicates(lst)}')
    print(f'고유 원소 개수: {count_unique([1, 2, 3, 2], [3, 4, 5, 4])}')
